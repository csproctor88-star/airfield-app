// One-time generator: turns the prototype data in public/training into
//   1) committed JSON seed files under lib/amtr/data/  (app one-click seed)
//   2) supabase/migrations/2026052006_amtr_demo_seed.sql  (Demo AFB pre-seed:
//      full catalogs + several sample members with representative progress)
//
// Run: node scripts/extract-amtr-seed.mjs
import fs from 'node:fs'
import path from 'node:path'

const ROOT = path.resolve(import.meta.dirname, '..')
const TRAIN = path.join(ROOT, 'public', 'training')
const DATA_DIR = path.join(ROOT, 'lib', 'amtr', 'data')
const MIGRATION = path.join(ROOT, 'supabase', 'migrations', '2026052006_amtr_demo_seed.sql')

const state = JSON.parse(fs.readFileSync(path.join(TRAIN, 'amtr-state.json'), 'utf8'))

// JQS catalog lives in jqs-catalog-data.js as `window.__JQS_CATALOG = {...};`
const jqsRaw = fs.readFileSync(path.join(TRAIN, 'src', 'jqs-catalog-data.js'), 'utf8')
const jqsJson = jqsRaw.slice(jqsRaw.indexOf('{'), jqsRaw.lastIndexOf('}') + 1)
const jqsCat = JSON.parse(jqsJson).catalog

// ── Normalize into our schema shapes ───────────────────────
const jqs = jqsCat.map((r, i) => ({
  kind: r.kind, number: r.number ?? null, title: r.title ?? r.fullTitle ?? '',
  depth: (r.depth ?? 1) - 1, required: false,
  training_refs: r.tr ?? null, core_cert: r.coreCert ?? null, deploy_sei: r.deploySei ?? null,
  prof3: r.prof3 ?? null, prof5: r.prof5 ?? null, prof7: r.prof7 ?? null, prof9: r.prof9 ?? null,
  sort_order: i,
}))

const r1098 = state.rt1098.tasks.map((t, i) => ({
  task: t.task, type: t.type || null, frequency: t.frequency || 'Annual', sort_order: i,
}))

const formal = []
;['haf', 'initial', 'continuation'].forEach((section) => {
  ;(state.formalCatalog[section] || []).forEach((c, i) =>
    formal.push({ section, course: c.course, sort_order: i }))
})

// RAT catalog from the sample member's rat items (13 standard courses)
const ratItems = state.members[0]?.rat?.items ?? state.members[0]?.rat ?? []
const rat = ratItems.map((c, i) => ({
  course: c.course, category: c.category || null, method: c.method || null,
  frequency: 'Annual', sort_order: i,
}))

const milestones = []
const PATHS = ['fiveLevelQtp', 'amosAmslPcg', 'sevenLevelQtp', 'afmPcg']
const m0 = state.members[0]?.milestones || {}
PATHS.forEach((p) => {
  let order = 0
  ;(m0[p]?.phases || []).forEach((ph) => {
    ;(ph.items || []).forEach((it) =>
      milestones.push({ path: p, phase_label: ph.label, sts_items: it.stsItems || null, topic: it.topic || '', sort_order: order++ }))
  })
})

// ── Write JSON seed files ──────────────────────────────────
fs.mkdirSync(DATA_DIR, { recursive: true })
const writeJson = (name, obj) => fs.writeFileSync(path.join(DATA_DIR, name), JSON.stringify(obj, null, 0))
writeJson('jqs-catalog.json', jqs)
writeJson('recurring-1098.json', r1098)
writeJson('formal-courses.json', formal)
writeJson('rat-courses.json', rat)
writeJson('milestones.json', milestones)

console.log(`JSON written: jqs ${jqs.length}, 1098 ${r1098.length}, formal ${formal.length}, rat ${rat.length}, milestones ${milestones.length}`)

